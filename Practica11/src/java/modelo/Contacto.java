package modelo;

/**
 *
 * @author marga
 */
public class Contacto {
    
    public Contacto(){
    }
    
    public Contacto(String fname, String email, String phone) {
        this.fname = fname;
        this.email = email;
        this.phone = phone;
    }
    public Contacto(int id, String fname, String email, String phone) {
        this.id = id;
        this.fname = fname;
        this.email = email;
        this.phone = phone;
    }

    /**
     * @return the id
     */
    public int getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(int id) {
        this.id = id;
    }

    /**
     * @return the fname
     */
    public String getFname() {
        return fname;
    }

    /**
     * @param fname the fname to set
     */
    public void setFname(String fname) {
        this.fname = fname;
    }

    /**
     * @return the phone
     */
    public String getPhone() {
        return phone;
    }

    /**
     * @param phone the phone to set
     */
    public void setPhone(String phone) {
        this.phone = phone;
    }

    /**
     * @return the email
     */
    public String getEmail() {
        return email;
    }

    /**
     * @param email the email to set
     */
    public void setEmail(String email) {
        this.email = email;
    }
    private int id;
    private String fname;
    private String phone;
    private String email;
}
